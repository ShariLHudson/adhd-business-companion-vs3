export const PRONOUN_ONLY =
  /^(?:that|this|it|those|these|they|them|he|she)\.?$/i;

export function isStopWordTopic(token: string): boolean {
  const t = token.toLowerCase().replace(/[^a-z0-9'-]/g, "");
  if (!t || t.length < 3) return true;
  return TOPIC_STOP_WORDS.has(t);
}

export function isPronounAsTopic(text: string): boolean {
  return PRONOUN_ONLY.test(text.trim());
}
