export function listSampleOpportunities(): Opportunity[] {
  return [...SAMPLE_OPPORTUNITIES];
}

export function getSampleSignalsForOpportunity(opportunityId: OpportunityId): OpportunitySignal[] {
  return SAMPLE_OPPORTUNITY_SIGNALS.filter((s) => s.opportunityId === opportunityId);
}

export function getSampleEvidenceForOpportunity(opportunityId: OpportunityId): OpportunityEvidence[] {
  return SAMPLE_OPPORTUNITY_EVIDENCE.filter((e) => e.opportunityId === opportunityId);
}

export function getSampleRecommendationsForOpportunity(
  opportunityId: OpportunityId,
): OpportunityRecommendation[] {
  const opp = getSampleOpportunity(opportunityId);
  if (!opp) return [];
  return SAMPLE_OPPORTUNITY_RECOMMENDATIONS.filter((r) => opp.recommendationIds.includes(r.id));
}

export function getSampleRisksForOpportunity(opportunityId: OpportunityId): OpportunityRisk[] {
  const opp = getSampleOpportunity(opportunityId);
  if (!opp) return [];
  return SAMPLE_OPPORTUNITY_RISKS.filter((r) => opp.riskIds.includes(r.id));
}

export function getSampleRelationshipsForOpportunity(
  opportunityId: OpportunityId,
): OpportunityRelationship[] {
  return SAMPLE_OPPORTUNITY_RELATIONSHIPS.filter(
    (r) => r.fromOpportunityId === opportunityId || r.toOpportunityId === opportunityId,
  );
}
